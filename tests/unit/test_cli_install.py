def test_cli_install_happy_path(tmp_path, capsys):
    rc = main(
        [
            "install",
            "--prefix",
            str(tmp_path),
            "--bin",
            "/opt/nanio",
        ]
    )
    assert rc == 0
    out = capsys.readouterr().out
    assert "nanio installed." in out
    assert "access_key = " in out
    assert "secret_key = " in out
    assert "systemctl daemon-reload" in out
    assert "systemctl enable" in out
    assert "http://0.0.0.0:9000" in out
    # Files exist on disk.
    assert (tmp_path / "etc/nanio/options.toml").is_file()
    assert (tmp_path / "etc/systemd/system/nanio.service").is_file()


def test_cli_install_refuses_to_clobber_returns_2(tmp_path, capsys):
    main(["install", "--prefix", str(tmp_path), "--bin", "/opt/nanio"])
    capsys.readouterr()  # drain stdout from the first run
    rc = main(["install", "--prefix", str(tmp_path), "--bin", "/opt/nanio"])
    assert rc == 2
    err = capsys.readouterr().err
    assert "refusing to overwrite" in err
    assert "--force" in err


def test_cli_install_force_overrides(tmp_path, capsys):
    main(["install", "--prefix", str(tmp_path), "--bin", "/opt/nanio"])
    capsys.readouterr()
    rc = main(
        [
            "install",
            "--prefix",
            str(tmp_path),
            "--bin",
            "/opt/nanio",
            "--force",
        ]
    )
    assert rc == 0


def test_cli_install_warns_when_bin_path_was_guessed(tmp_path, capsys, monkeypatch):
    # Force detect_bin_path to fall through to the placeholder path.
    monkeypatch.setattr("sys.argv", [""])
    monkeypatch.setattr("shutil.which", lambda name: None)
    rc = main(["install", "--prefix", str(tmp_path)])
    assert rc == 0
    out = capsys.readouterr().out
    assert "WARNING" in out
    assert "/usr/local/bin/nanio" in out


def test_cli_install_subcommand_help(capsys):
    parser = build_parser()
    with pytest.raises(SystemExit):
        parser.parse_args(["install", "--help"])
    out = capsys.readouterr().out
    assert "--prefix" in out
    assert "--data-dir" in out
    assert "--force" in out


def test_cli_install_custom_user_and_host_propagate(tmp_path, capsys):
    rc = main(
        [
            "install",
            "--prefix",
            str(tmp_path),
            "--bin",
            "/opt/nanio",
            "--user",
            "objectstore",
            "--host",
            "127.0.0.1",
            "--port",
            "8888",
        ]
    )
    assert rc == 0
    body = (tmp_path / "etc/systemd/system/nanio.service").read_text()
    assert "User=objectstore" in body
    assert "Group=objectstore" in body
    # host/port no longer appear in the unit file — they live in options.toml.
    server = load_server_options(tmp_path / "etc/nanio/options.toml")
    assert server["host"] == "127.0.0.1"
    assert server["port"] == 8888
    out = capsys.readouterr().out
    assert "http://127.0.0.1:8888" in out
