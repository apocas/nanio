def test_install_refuses_to_clobber_options(tmp_path):
    install(prefix=tmp_path, bin_path=Path("/opt/nanio"))
    with pytest.raises(FileExistsError) as ei:
        install(prefix=tmp_path, bin_path=Path("/opt/nanio"))
    assert "options.toml" in str(ei.value)


def test_install_refuses_to_clobber_unit_file_only(tmp_path):
    """Even if only the unit exists, the install must still refuse."""
    unit_dir = tmp_path / "etc/systemd/system"
    unit_dir.mkdir(parents=True)
    (unit_dir / "nanio.service").write_text("placeholder")
    with pytest.raises(FileExistsError):
        install(prefix=tmp_path, bin_path=Path("/opt/nanio"))


def test_install_force_overwrites_existing(tmp_path):
    r1 = install(prefix=tmp_path, bin_path=Path("/opt/nanio"))
    r2 = install(prefix=tmp_path, bin_path=Path("/opt/nanio"), force=True)
    assert r1.access_key != r2.access_key
    assert r2.options_path.read_text().count(r2.access_key) == 1
    assert r1.access_key not in r2.options_path.read_text()


def test_install_auto_detects_bin_when_not_passed(tmp_path, monkeypatch):
    """install(bin_path=None) → detect_bin_path() → use the result."""
    fake_bin = tmp_path / "nanio"
    fake_bin.write_text("#!/bin/sh\n")
    fake_bin.chmod(0o755)
    monkeypatch.setattr("sys.argv", [str(fake_bin)])
    result = install(prefix=tmp_path / "install")
    assert result.bin_path == fake_bin.resolve()
    assert result.bin_was_guessed is False


