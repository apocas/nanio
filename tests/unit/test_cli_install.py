def test_detect_bin_path_uses_argv0_when_real(tmp_path, monkeypatch):
    fake_bin = tmp_path / "nanio"
    fake_bin.write_text("#!/bin/sh\nexec true\n")
    fake_bin.chmod(0o755)
    monkeypatch.setattr("sys.argv", [str(fake_bin)])
    path, guessed = detect_bin_path()
    assert path == fake_bin.resolve()
    assert guessed is False


def test_detect_bin_path_falls_back_to_which(tmp_path, monkeypatch):
    monkeypatch.setattr("sys.argv", [""])
    fake_bin = tmp_path / "nanio"
    fake_bin.write_text("#!/bin/sh\n")
    fake_bin.chmod(0o755)
    monkeypatch.setattr("shutil.which", lambda name: str(fake_bin) if name == "nanio" else None)
    path, guessed = detect_bin_path()
    assert path == fake_bin.resolve()
    assert guessed is False


def test_detect_bin_path_last_resort_when_nothing_found(monkeypatch):
    monkeypatch.setattr("sys.argv", [""])
    monkeypatch.setattr("shutil.which", lambda name: None)
    path, guessed = detect_bin_path()
    assert path == Path("/usr/local/bin/nanio")
    assert guessed is True


def test_detect_bin_path_argv0_not_named_nanio(tmp_path, monkeypatch):
    """argv[0] points at a real file but not named `nanio` — fall through."""
    other = tmp_path / "python"
    other.write_text("#")
    other.chmod(0o755)
    monkeypatch.setattr("sys.argv", [str(other)])
    monkeypatch.setattr("shutil.which", lambda name: None)
    _, guessed = detect_bin_path()
    assert guessed is True


