def test_generate_credentials_returns_two_distinct_strings():
    ak, sk = generate_credentials()
    assert isinstance(ak, str) and isinstance(sk, str)
    assert ak != sk
    # Loose AWS-shape sanity: 20-ish access key, 40-ish secret key.
    assert 18 <= len(ak) <= 24
    assert 38 <= len(sk) <= 44


def test_generate_credentials_is_random_across_calls():
    pairs = {generate_credentials() for _ in range(20)}
    assert len(pairs) == 20


def test_render_options_toml_round_trips_through_resolvers(tmp_path):
    body = render_options_toml(
        access_key="alice",
        secret_key="wonderland",
        data_dir=Path("/var/lib/nanio"),
        host="127.0.0.1",
        port=9001,
        region="eu-west-1",
    )
    p = tmp_path / "options.toml"
    p.write_text(body)

    resolver = TomlFileCredentialResolver(p)
    assert resolver.resolve("alice") == "wonderland"
    assert resolver.resolve("bob") is None

    server = load_server_options(p)
    assert server == {
        "data_dir": "/var/lib/nanio",
        "host": "127.0.0.1",
        "port": 9001,
        "region": "eu-west-1",
    }


def test_render_options_toml_uses_default_region_when_not_passed(tmp_path):
    body = render_options_toml(
        access_key="ak",
        secret_key="sk",
        data_dir=Path("/var/lib/nanio"),
        host="0.0.0.0",
        port=9000,
    )
    p = tmp_path / "options.toml"
    p.write_text(body)
    server = load_server_options(p)
    assert server["region"] == "us-east-1"


def test_render_options_toml_includes_helper_comments():
    """The generated file documents how to add more users."""
    body = render_options_toml(
        access_key="ak",
        secret_key="sk",
        data_dir=Path("/var/lib/nanio"),
        host="0.0.0.0",
        port=9000,
    )
    assert "# Add more users" in body
    assert "# nanio options. Generated by `nanio install`." in body


def test_render_systemd_unit_substitutes_all_fields():
    unit = render_systemd_unit(
        bin_path=Path("/opt/nanio/bin/nanio"),
        options_path=Path("/etc/nanio/options.toml"),
        data_dir=Path("/srv/nanio"),
        user="customuser",
    )
    assert "ExecStart=/opt/nanio/bin/nanio serve --options /etc/nanio/options.toml" in unit
    assert "User=customuser" in unit
    assert "Group=customuser" in unit
    assert "ReadWritePaths=/srv/nanio" in unit
    assert "[Install]" in unit
    assert "WantedBy=multi-user.target" in unit
    # No --host/--port/--data-dir on the command line — those live in options.toml.
    assert "--host" not in unit
    assert "--port" not in unit
    assert "--data-dir" not in unit


